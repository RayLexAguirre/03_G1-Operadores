//operadores aritmeticos 
//suma
console.log(5 + 7);
//resta
console.log(5 - 7);
//multiplicacion
console.log(5 * 7);
//potenciacion
console.log(2 ** 3);
//divicion
console.log(5 / 2);
//residuo de la division 
console.log(5 % 2);
console.log(7 % 3);

//operadores de asignacion 
let a = 5 + 2;

console.log(a);

//sumarle a la variable anterior 
a = a + 1; 
console.log(a);

//forma avrebiada de decir lo anterior sumarle 1
a++;

console.log(a);

a = a - 1; 
console.log(a);

//este es el mismo caso solo que restarle 1
a--;
console.log(a);